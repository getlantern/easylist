func TestBlock(t *testing.T) {
	l, err := Open("easylist.txt", 5*time.Minute)
	if !assert.NoError(t, err) {
		return
	}

	_l := l.(*list)
	assert.True(t, _l.domainMatchers.Len() > 1000, "List has too few domains")
	m, _ := _l.domainMatchers.Get(reverse("cdn.adblade.com"))
	assert.NotNil(t, m)

	req, _ := http.NewRequest("GET", "http://osnews.com", nil)
	assert.True(t, l.Allow(req), "Domain that doesn't appear on list should be allowed")

	// Not currently checking non-domain-specific rules
	// req, _ = http.NewRequest("GET", "http://somedomain.com/adwords/stuff", nil)
	// assert.False(t, l.Allow(req))

	req, _ = http.NewRequest("GET", "https://cdn.adblade.com", nil)
	assert.False(t, l.Allow(req), "Domain on list should not be allowed")

	req, _ = http.NewRequest("GET", "http://cdn.adblade.com:8080", nil)
	assert.False(t, l.Allow(req), "Domain on list should not be allowed")

	req, _ = http.NewRequest("GET", "https://cdn.adblade.com:443", nil)
	assert.False(t, l.Allow(req), "Domain on list should not be allowed")

	req, _ = http.NewRequest("GET", "https://subdomain.cdn.adblade.com:443", nil)
	assert.False(t, l.Allow(req), "Subdomain of domain on list should not be allowed")

	req, _ = http.NewRequest("GET", "https://subdomaincdn.adblade.com:443", nil)
	assert.True(t, l.Allow(req), "Superset name of domain on list (looks like subdomain without dot separator) should be allowed")

	req, _ = http.NewRequest("GET", "https://c-sharpcorner.com/something/allowed", nil)
	assert.True(t, l.Allow(req), "Domain with path not matching rule should be allowed")

	req, _ = http.NewRequest("GET", "https://c-sharpcorner.com/stuff/banners/", nil)
	assert.False(t, l.Allow(req), "Domain with path matching rule should not be allowed")

	req, _ = http.NewRequest("GET", "https://subdomain.c-sharpcorner.com/stuff/banners/", nil)
	assert.False(t, l.Allow(req), "Subdomain with path matching rule should not be allowed")

	req, _ = http.NewRequest("GET", "https://subdomain.google.com/pagead/imgad?id=CICAgKDLm4jqFBABGAEyCIOA1Ft_hUOC", nil)
	assert.False(t, l.Allow(req), "Subdomain with path matching rule should not be allowed")

	req, _ = http.NewRequest("GET", "https://s3.amazonaws.com", nil)
	assert.True(t, l.Allow(req), "Domain should be allowed by default")

	req, _ = http.NewRequest("GET", "https://tnaflix.com/ad/index.html", nil)
	assert.True(t, l.Allow(req), "Rule with unsupported object-subrequest option should be ignored")

	req, _ = http.NewRequest("GET", "https://vidible.tv/prod/index.html", nil)
	assert.True(t, l.Allow(req), "Rule with unsupported other option should be ignored")

	req, _ = http.NewRequest("GET", "https://s1.wp.com/index.html", nil)
	assert.True(t, l.Allow(req), "Rule with unsupported subdocument option should be ignored")

	req, _ = http.NewRequest("GET", "https://redtube.com/gallery/index.html", nil)
	assert.True(t, l.Allow(req), "Rule with unsupported xmlhttprequest option should be ignored")

	req, _ = http.NewRequest("GET", "https://s3.amazonaws.com", nil)
	assert.True(t, l.Allow(req), "Domain should be allowed by default")

	req.Header.Set("Origin", "https://gaybeeg.info")
	assert.False(t, l.Allow(req), "Domain loaded from specific domains should not be allowed")
}

func TestEmpty(t *testing.T) {
	l := &list{}
	req, _ := http.NewRequest("GET", "https://random-site.com", nil)
	assert.True(t, l.Allow(req), "Domain should be allowed if list is empty")
}

func BenchmarkPass(b *testing.B) {
	l, err := Open("easylist.txt", 5*time.Minute)
	if err != nil {
		b.Fatalf("Unable to open easylist: %v", err)
	}

	// After loading rules, start CPU profiling
	os.Remove("benchmark.cpuprofile")
	cp, err := os.Create("benchmark.cpuprofile")
	if err != nil {
		b.Fatalf("Unable to create benchmark.cpuprofile: %v", err)
	}
	err = pprof.StartCPUProfile(cp)
	if err != nil {
		b.Fatalf("Unable to start CPU profiling: %v", err)
	}
	defer pprof.StopCPUProfile()

	b.ResetTimer()
	req, _ := http.NewRequest("GET", "http://osnews.com", nil)
	for i := 0; i < b.N; i++ {
		l.Allow(req)
	}
}

